// Crear un programa que me calcule cuál es la suma entre dos números ingresados por el usuario.

const numero1 = parseInt(prompt("Ingrese el primer numero"));
const numero2 = parseInt(prompt("Ingrese el segundo numero"));

const suma = numero1 + numero2;
console.log(suma);

// y si quiero calcular el residuo de la división entre esos dos números?

const residuo = numero1 % numero2;
console.log(residuo);
